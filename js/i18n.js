/* =========================================================================
   Registry — peta kode bahasa ke paket terjemahan.
   Bahasa dari languages.js yang TIDAK ada di sini otomatis memakai EN.
   ========================================================================= */
const I18N_PACKS = {
  en: EN, id: ID, ms: MS, es: ES, fr: FR, de: DE, pt: PT, it: IT, nl: NL, ru: RU,
  "zh-CN": ZH_CN, "zh-TW": ZH_TW, ja: JA, ko: KO, ar: AR, hi: HI, vi: VI, th: TH,
  tl: TL, bn: BN, ta: TA, ur: UR, fa: FA, he: HE, tr: TR, uk: UK, pl: PL, cs: CS,
  sk: SK, hu: HU, ro: RO, bg: BG, sr: SR, el: EL, sv: SV, no: NO, da: DA, fi: FI,
  sw: SW, ha: HA, af: AF, zu: ZU, am: AM,
};

function getPack(code) {
  return I18N_PACKS[code] || EN;
}

/** Mengambil teks terjemahan dengan fallback bertingkat: bahasa terpilih -> EN -> key itu sendiri. */
function t(key) {
  const lang = (window.APP_STATE && window.APP_STATE.settings && window.APP_STATE.settings.lang) || "id";
  const pack = getPack(lang);
  const val = key.split(".").reduce((o, k) => (o && o[k] !== undefined ? o[k] : undefined), pack);
  if (val !== undefined) return val;
  const enVal = key.split(".").reduce((o, k) => (o && o[k] !== undefined ? o[k] : undefined), EN);
  return enVal !== undefined ? enVal : key;
}

/** Ganti placeholder {nama} di dalam string terjemahan. */
function tf(key, params) {
  let str = t(key);
  if (params) {
    Object.keys(params).forEach((k) => {
      str = str.replace(`{${k}}`, params[k]);
    });
  }
  return str;
}
